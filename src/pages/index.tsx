import type { NextPage } from "next";
import Head from "next/head";
import Image from "next/image";
import { useSession, signIn, signOut } from "next-auth/react";
import { AuthStatus } from "../types/custom-next-auth";
import { Spinner } from "../components/spinner/spinner";
import { trpc } from "../utils/trpc";
import { FC } from "react";
import { Session } from "next-auth";
import Link from "next/link";

export const UserInfo: FC<{ session: Session | null }> = ({ session }) => {
  return (
    <div className="flex w-full">
      <div className="w-48">
        <div className="flex flex-row justify-between">
          <div className="flex flex-col">
            <h1 className="prose-xl">{session?.user?.name}</h1>
            <p className="prose-md">{session?.user?.email}</p>
          </div>
        </div>
        <div className="flex justify-between">
          <button className="btn" onClick={() => signOut()}>
            Sign out
          </button>
          {session?.user?.image ? (
            <Image
              className="rounded-full"
              src={session?.user?.image}
              width={50}
              height={50}
              alt="User image"
            />
          ) : null}
        </div>
      </div>
    </div>
  );
};

export const FolderCard: FC<{ name: string; folderId: string }> = ({
  name,
  folderId,
}) => {
  return (
    <Link href={`/folder/${folderId}`}>
      <div className="card h-60 w-60 cursor-pointer bg-base-100 shadow-xl">
        <figure></figure>
        <div className="card-body">
          <h2 className="card-title">
            {name}
            <div className="badge badge-secondary">NEW</div>
            <div>üìÅ</div>
          </h2>
          <p>If a dog chews shoes whose shoes does he choose?</p>
          <div className="card-actions justify-end"></div>
        </div>
      </div>
    </Link>
  );
};

const Home: NextPage = (props) => {
  const { data: session, status } = useSession();

  const { data, isLoading, error } = trpc.useQuery([
    "protected.get-my-folders",
  ]);

  if (status === AuthStatus.LOADING || isLoading) {
    return <Spinner />;
  }

  return (
    <>
      <Head>
        <title>Create T3 App</title>
        <meta name="description" content="Generated by create-t3-app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <main>
        {session ? (
          <div className="ml-4 flex w-full flex-col">
            <UserInfo session={session} />
            <div className="flex flex-wrap gap-4">
              {data?.map((folder) => (
                <FolderCard
                  key={folder?.id}
                  name={folder?.name}
                  folderId={folder?.id}
                />
              ))}
            </div>
          </div>
        ) : (
          <>
            Not signed in <br />
            <button className="btn" onClick={() => signIn()}>
              Sign in
            </button>
          </>
        )}
      </main>
    </>
  );
};

export default Home;
